package Esercitazione1;

public class Esercizio2 {
    public static void main(String[] args) {

        int a = 7;
        int b = 6;

        System.out.println("Il primo numero è: " + a);

        System.out.println("Il secondo numero è: " + b);

        // ----------operaizioni

        int sum = a + b;
        int diff = a - b;
        int molt = a * b;
        int div = a / b;

        // ------------

        System.out.println("Il risultato di 7 + 6 è: " + sum);

        System.out.println("Il risultato di 7 - 6 è: " + diff);

        System.out.println("Il risultato di 7 * 6 è: " + molt);

        System.out.println("Il risultato di 7 / 6 è: " + div);

    }
}
